export type Binary = 0 | 1;
export type Image = Binary[][];

export interface Input {
  algorithm: Binary[],
  image: Image
}

const mapToBinary = (line: string): Binary[] => {
  return line.split('').map(char => char === '#' ? 1 : 0);
  ;
};

const transform = (input: string): Input => {
  const [algoString, imageString] = input.trim().split('\n\n');
  return {
    algorithm: mapToBinary(algoString),
    image: imageString.split('\n').map(mapToBinary)
  };
};

